fun LargeSize.toSize(): Size {

    return Size(
        mainBoxHeight = mainBoxHeight,
        mainBoxWidth = mainBoxWidth,
        blackCircleSize = blackCircleSize,
        blackCirclePaddingBottom = blackCirclePaddingBottom,
        yellowCircleSize = yellowCircleSize,
        yellowCirclePaddingVertical = yellowCirclePaddingVertical,
        yellowCirclePaddingHorizontal = yellowCirclePaddingHorizontal,
        userImageSize = userImageSize,
        editButtonPadding = editButtonPadding,
        editButtonIconSize = editButtonIconSize,
        editIconPadding = editIconPadding
